  function toUint32(ip) {
    var p = ip.split('.');
    if (p.length !== 4) return 0;
    return (((+p[0] & 0xFF) * 16777216 +
              (+p[1] & 0xFF) * 65536 +
              (+p[2] & 0xFF) * 256 +
               +p[3] & 0xFF) >>> 0);
  }

  global.ip2country = function (ip) {
    if (!ip || typeof ip !== 'string') return '不明';
    var n = toUint32(ip);
    for (var i = 0; i < SPECIAL.length; i++) {
      if (n >= SPECIAL[i][0] && n <= SPECIAL[i][1]) return SPECIAL[i][2];
    }
    return OCTET1[n >>> 24] || '不明';
  };

